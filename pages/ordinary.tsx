function Game() {
  const [streak, setStreak] = useState(0);
  const [guess, setGuess] = useState("");
  const [currentResult, setCurrentResult] = useState("");
  const [gameOver, setGameOver] = useState(false);
  const [finalResult, setFinalResult] = useState("");
  const [randWord, setRandWord] = useState("");
  const [nextWord, setNextWord] = useState("");

  // Select a new word when the game starts or when the user submits a correct guess
  useEffect(() => {
    const randomIndex = Math.floor(Math.random() * words.length);
    setRandWord(words[randomIndex]);

    // Get the index of the next word in the list
    const nextWordIndex = (randomIndex + 1) % words.length;

    // Get the next word in the list
    setNextWord(words[nextWordIndex]);
  }, [gameOver]);

  function handleGuess() {
    if (guess === nextWord) {
      setStreak(streak + 1);
      setCurrentResult(``);
      const randomIndex = Math.floor(Math.random() * words.length);
      setRandWord(words[randomIndex]);
  
      // Get the index of the next word in the list
      const nextWordIndex = (randomIndex + 1) % words.length;
  
      // Get the next word in the list
      setNextWord(words[nextWordIndex]);
    } else {
      setGameOver(true);
      setFinalResult(`Game over! Your streak was ${streak}.`);
    }
  }

  function handleSubmit(e) {
    e.preventDefault();
    handleGuess(guess);
  }

  function handlePlayAgain() {
    setGameOver(false);
    setStreak(0);
    setCurrentResult("");
    setFinalResult("");
  }

  return (
    <div className="grid py-48 h-screen place-items-center">
      {!gameOver ? (
        <>
          <div className="flex flex-col justify-center items-center">
          <p className="bg-gray-800 p-5 rounded-md text-6xl w-48 text-center">{randWord}</p>
          </div>
          <p className="">{streak}</p>
          <form className="flex flex-col justify-center items-center" onSubmit={handleSubmit}>
            <input
              className="text-black"
              type="text"
              value={guess}
              onChange={(e) => setGuess(e.target.value)}
            />
            <button className="bg-gray-600 p-2 px-5 my-10 rounded-full flex items-center justify-center" type="submit">Guess</button>
          </form>
          <p>{currentResult}</p>
        </>
      ) : (
        <>
          <div className="flex flex-col justify-center items-center">
            <p className="">{finalResult}</p>
            <button className="bg-gray-600 p-2 px-5 my-10 rounded-full" onClick={handlePlayAgain}>Play Again</button>
          </div>
        </>

      )}
    </div>
  );
}

export default Game;
